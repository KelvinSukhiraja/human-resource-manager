	package com.hr.pojo;

public class Notice {
	private int idnotices;
	private String notice_name;
	private String notice_content;
	private String notice_creation_time;
	private String notice_publisher;
	
	public int getIdnotices() {
		return idnotices;
	}
	public void setIdnotices(int idnotices) {
		this.idnotices = idnotices;
	}
	public String getNotice_name() {
		return notice_name;
	}
	public void setNotice_name(String notice_name) {
		this.notice_name = notice_name;
	}
	public String getNotice_content() {
		return notice_content;
	}
	public void setNotice_content(String notice_content) {
		this.notice_content = notice_content;
	}
	public String getNotice_creation_time() {
		return notice_creation_time;
	}
	public void setNotice_creation_time(String notice_creation_time) {
		this.notice_creation_time = notice_creation_time;
	}
	public String getNotice_publisher() {
		return notice_publisher;
	}
	public void setNotice_publisher(String notice_publisher) {
		this.notice_publisher = notice_publisher;
	}
	
	
}
