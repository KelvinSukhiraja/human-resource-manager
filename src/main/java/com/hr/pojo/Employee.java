package com.hr.pojo;

public class Employee {
	private int employee_id;
	private String employee_address; 
	private String employee_name; 
	private String employee_education; 
	private String employee_gender;
	private String employee_job;
	private String employee_created_date;
	private String employee_email;
	private String employee_dept;
	private Long employee_phone;
	
	public int getEmployee_id() {
		return employee_id;
	}
	public void setEmployee_id(int employee_id) {
		this.employee_id = employee_id;
	}
	public String getEmployee_address() {
		return employee_address;
	}
	public void setEmployee_address(String employee_address) {
		this.employee_address = employee_address;
	}
	public String getEmployee_name() {
		return employee_name;
	}
	public void setEmployee_name(String employee_name) {
		this.employee_name = employee_name;
	}
	public String getEmployee_education() {
		return employee_education;
	}
	public void setEmployee_education(String employee_education) {
		this.employee_education = employee_education;
	}
	public String getEmployee_gender() {
		return employee_gender;
	}
	public void setEmployee_gender(String employee_gender) {
		this.employee_gender = employee_gender;
	}
	
	public String getEmployee_job() {
		return employee_job;
	}
	public void setEmployee_job(String employee_job) {
		this.employee_job = employee_job;
	}
	public String getEmployee_created_date() {
		return employee_created_date;
	}
	public void setEmployee_created_date(String employee_created_date) {
		this.employee_created_date = employee_created_date;
	}
	public String getEmployee_email() {
		return employee_email;
	}
	public void setEmployee_email(String employee_email) {
		this.employee_email = employee_email;
	}
	public String getEmployee_dept() {
		return employee_dept;
	}
	public void setEmployee_dept(String employee_dept) {
		this.employee_dept = employee_dept;
	}
	public Long getEmployee_phone() {
		return employee_phone;
	}
	public void setEmployee_phone(Long employee_phone) {
		this.employee_phone = employee_phone;
	}
	
	
}
